package Anotacoes;

public class Anotacao09 {
    public static void main(String[] args) {

        //OPERAÇÕES BIT A BIT
        // COMPARA OS NUMERO EM FORMA BINARIA

        int n1 = 89;
        int n2 = 60;

        System.out.println(n1 & n2);
        System.out.println(n1 | n2);
        System.out.println(n1 ^ n2);
    }
}
